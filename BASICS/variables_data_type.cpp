#include <iostream>

int main() {
    int age = 25; // Declare an integer variable named 'age' and initialize it with the value 25
    float height = 1.75; // Declare a float variable named 'height' and initialize it with the value 1.75
    char grade = 'A'; // Declare a char variable named 'grade' and initialize it with the value 'A'

    std::cout << "Age: " << age << std::endl; // Print the value of 'age' to the console
    std::cout << "Height: " << height << " meters" << std::endl; // Print the value of 'height' to the console with a unit
    std::cout << "Grade: " << grade << std::endl; // Print the value of 'grade' to the console
}